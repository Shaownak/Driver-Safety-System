def display_lines(image, lines):
    lines_image = np.zeros_like(image, dtype=np.uint8)  # Ensure the dtype is uint8
    if lines is not None:
        for line in lines:
            if len(line) >= 4:  # Check if there are at least four coordinates
                x1, y1, x2, y2 = line[:4]  # Extract the first four coordinates
                # Convert coordinates to integers
                x1, y1, x2, y2 = int(x1), int(y1), int(x2), int(y2)
                # Draw lines on the lines_image
                cv2.line(lines_image, (x1, y1), (x2, y2), (255, 0, 0), 10)
    return lines_image




def average(image, lines):
    left = []
    right = []

    if lines is not None:
        for line in lines:
            x1, y1, x2, y2 = line[0]  # Extract line coordinates
            # Fit a line to the points and return slope and y-intercept
            parameters = np.polyfit((x1, x2), (y1, y2), 1)
            slope = parameters[0]
            y_int = parameters[1]
            # Lines on the right have positive slope, and lines on the left have negative slope
            if slope < 0:
                left.append((x1, y1, x2, y2))  # Store the entire line segment
            else:
                right.append((x1, y1, x2, y2))  # Store the entire line segment

    # Check if any lines were detected
    if left and right:
        return np.array([left, right])
    else:
        return None  # Return None if no lines were detected






def make_points(image, average):
    print(average)
    slope, y_int = average
    y1 = image.shape[0]
    #how long we want our lines to be --> 3/5 the size of the image
    y2 = int(y1 * (3/5))
    #determine algebraically
    x1 = int((y1 - y_int) // slope)
    x2 = int((y2 - y_int) // slope)
    return np.array([x1, y1, x2, y2])



