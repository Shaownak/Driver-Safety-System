cap = cv2.VideoCapture(r'G:\Driver Safety\Driver_Safety\test_video\test.mp4')


while True:
    
    ret, image1 = cap.read()

    if not ret:
        break

    copy = np.copy(image1)
    edges = cv2.Canny(copy, 50, 150)
    isolated = region(edges)

    # Detect lines
    lines = cv2.HoughLinesP(isolated, 2, np.pi/180, 100, np.array([]), minLineLength=30, maxLineGap=5)

    if lines is not None:
        averaged_lines = average(copy, lines)
        black_lines = display_lines(copy, averaged_lines)
        lanes = cv2.addWeighted(copy, 0.8, black_lines, 1, 1)
        cv2.imshow("Lane Detection", lanes)
    else:
        # Handle the case where no lines are detected
        cv2.imshow("Lane Detection", copy)

    if cv2.waitKey(1) & 0xFF == ord('q'):
        break

cap.release()
cv2.destroyAllWindows()